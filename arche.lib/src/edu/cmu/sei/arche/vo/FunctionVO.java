package edu.cmu.sei.arche.vo;

import edu.cmu.sei.arche.corebridge.IRefreshableUI;

/**
 * Corresponds to fact MAIN::Function in ArchE core.
 * 
 * @author Neel Mullick
 */
public class FunctionVO extends CoreFact {

    /** Corresponds to slot description */
    private String description;

    /** Corresponds to slot id. This is not the fact id. It's just a value provided by the user. */
    private String id;

    /**
     * @return Returns the description.
     */
    public String getDescription() {
        return description;
    }

    /**
     * @param description The description to set.
     */
    public void setDescription(String description) {
        this.description = description;
        ui.flagViewsToRefresh(this.getClass());
    }

    /**
     * @return Returns the id.
     */
    public String getId() {
        return id;
    }

    /**
     * @param id The id to set.
     */
    public void setId(String id) {
        this.id = id;
        ui.flagViewsToRefresh(this.getClass());
    }

    /**
     * @param factId
     * @param factType
     * @param description
     * @param id
     */
    public FunctionVO(int factId, String description, String id, IRefreshableUI ui) {
        super(factId, "MAIN::Function", ui);
        this.description = description;
        this.id = id;
        ui.flagViewsToRefresh(this.getClass());
    }
}